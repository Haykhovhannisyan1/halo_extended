fn string_to_byte_array(s: &str) -> [u8; 11] {
    let mut array = [0u8; 11];
    array.copy_from_slice(s.as_bytes());

    array
}

fn get_gen() -> pallas::Affine {
    let hasher = pallas::Point::hash_to_curve("GENS");
    let my_array: [u8; 11] = string_to_byte_array("gen15_comit");
    let c = hasher(&my_array);
    return c.into();
}

